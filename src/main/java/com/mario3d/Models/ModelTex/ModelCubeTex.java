package com.mario3d.Models.ModelTex;

public class ModelCubeTex {
    public final int TexBaseX;
    public final int TexBaseY;
    public final int x;
    public final int y;
    public final int z;
    public ModelCubeTex(int TexBaseX, int TexBaseY, int x, int y, int z) {
        this.TexBaseX = TexBaseX;
        this.TexBaseY = TexBaseY;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
